private void btnMarcarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMarcarActionPerformed
    objHorarioJornadaContrato=PaqueteBusinessDelegate.getAsistenciaService().registrarAsistencia
            (txtUsuario.getText(), new String(txtPassword.getPassword()), tipoMarcado);
    if (objHorarioJornadaContrato!=null){
        frmMarcado.setObjHorarioJornadaContrato(objHorarioJornadaContrato);
        txtUsuario.setText("");
        txtPassword.setText("");
        txtUsuario.requestFocus();
    }
    else
    {
        frmMarcado.setObjHorarioJornadaContrato(null);
        txtUsuario.setText("");
        txtPassword.setText("");
        txtUsuario.requestFocus();
    }
    this.setVisible(false);
}//GEN-LAST:event_btnMarcarActionPerformed

