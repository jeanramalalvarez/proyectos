private void btnGenerarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarReporteActionPerformed
        
    
    if(cboTrabajador.getSelectedIndex()<=0){
        Mensaje.mensaje(this, "Debe seleccionar un trabajador",Mensaje.TITULO_ME, Mensaje.ERROR);
        return;
    }
    if(cboReportes.getSelectedIndex()<=0){
        Mensaje.mensaje(this, "Debe seleccionar un Reporte ",Mensaje.TITULO_ME, Mensaje.ERROR);
        return;
    }
    int idPersona=listaPersonal.get(cboTrabajador.getSelectedIndex()-1).getInt_idPersona();
    String strCodigoPersona=listaPersonal.get(cboTrabajador.getSelectedIndex()-1).getStr_codigo();
        if(cboReportes.getSelectedItem().toString().
                        equalsIgnoreCase("Memorandum")){
            if (!Filtro.opcionPermitida(gui.getLstFuncionesUsuario(), Opciones.getREPORTEMEMORANDUM())){
                Mensaje.mensaje(this,"Opci贸n no autorizada para su Usuario", Mensaje.TITULO_ME, Mensaje.ERROR);
                return;
            }
                    ireport.verMemorandumPersona(idPersona,"MemorandumPersona");
        }else if(cboReportes.getSelectedItem().toString().
                        equalsIgnoreCase("Funciones")){
            if (!Filtro.opcionPermitida(gui.getLstFuncionesUsuario(), Opciones.getREPORTEFUNCIONES())){
                Mensaje.mensaje(this,"Opci贸n no autorizada para su Usuario", Mensaje.TITULO_ME, Mensaje.ERROR);
                return;
            }

                if (ireport.verFuncionesXUsuario(strCodigoPersona,"FuncionesUsuario")){
                    Mensaje.mensaje(this,"Reporte generado "
                            , Mensaje.TITULO_ME, Mensaje.INFORMACION);
                }else{
                    Mensaje.mensaje(this,"Error al Generar Reporte", Mensaje.TITULO_ME, Mensaje.ERROR);
                }

        }else{
            if (!Filtro.opcionPermitida(gui.getLstFuncionesUsuario(), Opciones.getREPORTEPERMISOS())){
                Mensaje.mensaje(this,"Opci贸n no autorizada para su Usuario", Mensaje.TITULO_ME, Mensaje.ERROR);
                return;
            }
               ireport.verPermisoPersona(idPersona,"PermisoPersona");
        }
    
}//GEN-LAST:event_btnGenerarReporteActionPerformed

