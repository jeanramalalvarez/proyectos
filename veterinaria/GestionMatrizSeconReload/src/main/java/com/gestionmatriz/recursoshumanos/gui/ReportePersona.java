public void cargarComboTrabajador(){
    cboTrabajador.removeAllItems();
            Object objTitle=".::Seleccione un Trabajador::.";

            cboTrabajador.addItem(objTitle);
            
                if(cboReportes.getSelectedItem().toString().
                        equalsIgnoreCase("Memorandum")){
                    listaPersonal=PaqueteBusinessDelegate.
                            getRporteService().listaPersonaMemo();
                }else if(cboReportes.getSelectedItem().toString().
                        equalsIgnoreCase("Funciones")){
                listaPersonal=PaqueteBusinessDelegate.
                            getRporteService().listaPersonalFunciones();
                }else{
                listaPersonal=PaqueteBusinessDelegate.
                            getRporteService().listaPersonalPermisos();
                }
                        
                Object item;
                for (com.gestionmatriz.recursoshumanos.bean.Personal objPersona : listaPersonal) {
                    item=objPersona.getStr_codigo()+" "+objPersona.getStr_apePaterno()+" "+
                            objPersona.getStr_apeMaterno()+" "+objPersona.getStr_nombres();
                    cboTrabajador.addItem(item);
                }            
           
}


