    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String usuario = txtUsuario.getText().toUpperCase().trim();
        String password = String.valueOf(txtPassword.getPassword());

        try {
            Usuario objUsuario = PaqueteBusinessDelegate.getUsuarioService().validarUsuario(usuario, password);

            if (objUsuario != null) {
                Personal objPersona = new Personal();
                objPersona = PaqueteBusinessDelegate.getPersonalService().BuscarPersonaXCodigo(objUsuario.getStr_codigoPersona());
                gui.setObjPersonalHuella(objPersona);
                setVisible(false);                
            }
        } catch (SQLException ex) {
            log.error("Error al intentar verificar el usuario: " + ex.getMessage());            
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

