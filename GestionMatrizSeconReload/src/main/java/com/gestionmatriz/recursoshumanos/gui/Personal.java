    private void cargarFunciones() {
        try {

            List<Funciones> listaFunciones = PaqueteBusinessDelegate.getFuncionesService().
                    listaFunciones();



            int i = 0;
            DefaultListModel model = new DefaultListModel();
            Object rol;
            for (Funciones f : listaFunciones) {
                rol = f.getStr_rol();
                model.add(i, rol);
                i++;
            }

            lstFuncionesDisponiblesUsuario.setModel(model);


        } catch (SQLException ex) {
            log.error(ex);
        }



    }

