private void txtDniPersonalKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDniPersonalKeyTyped
    char c = evt.getKeyChar();

//    com.gestionmatriz.util.Util.focusText(evt,txtCarnetExtranjeriaPersonal);

    if (c == KeyEvent.VK_ENTER || c == KeyEvent.VK_TAB) {
        return;
    }

    if (!(Character.isDigit(c)
            || (c == KeyEvent.VK_BACK_SPACE)
            || (c == KeyEvent.VK_DELETE)) || txtDniPersonal.getText().trim().length() == 8) {
        Mensaje.mensaje(this, "Error: \n"
                + "•\tNo puede ingresar letras.\n "
                + "•\tSolo puede ingresar 8 dígitos para el DNI.",
                Mensaje.TITULO_ME, Mensaje.ERROR);
        getToolkit().beep();
        evt.consume();
    }
}//GEN-LAST:event_txtDniPersonalKeyTyped

