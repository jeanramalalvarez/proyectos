        if (index < 0) {
            return;
        }

        int modulo = listaModulo.get(index).getInt_modulo();
        listaFuncionesDisponibles = PaqueteBusinessDelegate.getFuncionesService().
                listaFuncionesXModulo(modulo);
        for (int i = 0; i < listaFuncionesDisponibles.size(); i++) {
            for (int j = 0; j < listaFuncionesAsignadas.size(); j++) {
//                    listaFuncionesAsignadas.get(j).setStr_usuarioCreador(gui.getBarraEstado().getUsuario());
                if (listaFuncionesDisponibles.get(i).getInt_idFunciones()
                        == listaFuncionesAsignadas.get(j).getInt_idFunciones()) {

                    listaFuncionesDisponibles.remove(i);
                    i--;

                    if (i < 0) {
                        break;
                    }
//                        if (i!=0){
//                        
//                        }  
//                        else
//                            break;
                }
            }
        }


        int i = 0;
        modelFuncionesDisponibles = new DefaultListModel();
        Object rol;
        for (Funciones f : listaFuncionesDisponibles) {
            rol = f.getStr_rol();
            modelFuncionesDisponibles.add(i, rol);
            i++;
        }

        lstFuncionesDisponiblesUsuario.setModel(modelFuncionesDisponibles);


        modelFuncionesAsignadas = new DefaultListModel();
        i = 0;
        for (Funciones f : listaFuncionesAsignadas) {
            if (f.getInt_modulo() == modulo) {
                rol = f.getStr_rol();
                modelFuncionesAsignadas.add(i, rol);
                i++;
            }

        }
        lstFuncionesAsignadasUsuario.setModel(modelFuncionesAsignadas);

