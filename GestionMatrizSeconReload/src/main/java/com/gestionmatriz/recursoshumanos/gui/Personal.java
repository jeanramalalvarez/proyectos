    private void cargarFuncionesUsuario(List<FuncionUsuario> lista) {
        int i = 0;
        modelFuncionesAsignadas = new DefaultListModel();

        Object rol;
        for (FuncionUsuario fu : lista) {
            rol = fu.getObjFunciones().getStr_rol();
            modelFuncionesAsignadas.add(i, rol);
            i++;
        }

        lstFuncionesAsignadasUsuario.setModel(modelFuncionesAsignadas);
    }

